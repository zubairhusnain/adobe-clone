              <p class="body-m">Adobe’s statements of disclosure articulate our policies and practices around recognizing and preventing human trafficking and slavery, in accordance with various requirements around the world. Read the <a href="/cc-shared/assets/pdf/corporate-responsibility/adobe-slavery-and-human-trafficking-policy-statement.pdf" target="_blank" daa-ll="UK Slavery and Human-7--Slavery and Human Tr">UK Slavery and Human Trafficking Policy Statement</a>, the <a href="/cc-shared/assets/pdf/corporate-responsibility/adobe-australia-modern-slavery-act-statement.pdf" target="_blank" daa-ll="Australia Modern Sla-8--Slavery and Human Tr">Australia Modern Slavery Act Statement</a>, and the <a href="/cc-shared/assets/pdf/corporate-responsibility/adobe-statement-on-the-california-transparency-in-supply-chains-act.pdf" target="_blank" daa-ll="California Transpare-9--Slavery and Human Tr">California Transparency in Supply Chains Act Statement</a>.</p>
