              <h3 id="adobe-codes-of-conduct" class="heading-s">Adobe Codes of Conduct</h3>
              <p class="body-m">Adobe has a comprehensive <a href="/cc-shared/assets/pdf/corporate-responsibility/code-of-conduct-ext.pdf" target="_blank" daa-ll="Code of Business Con-1--Adobe Codes of Condu">Code of Business Conduct</a> that sets standards and expectations for employee behavior both at work and in the marketplace. This code is based on our company values. We engage with business partners, suppliers and vendors who share our commitment to these principles. We’ve also adopted a comprehensive <a href="/cc-shared/assets/pdf/corporate-responsibility/adobe-business-partner-code-of-conduct.pdf" target="_blank" daa-ll="Business Partner Cod-2--Adobe Codes of Condu">Business Partner Code of Conduct</a>, derived from the <a href="http://www.unglobalcompact.org/index.html" data-http-link="true" daa-ll="United Nations Globa-3--Adobe Codes of Condu">United Nations Global Compact</a> and the <a href="https://www.responsiblebusiness.org/code-of-conduct/" daa-ll="Responsible Business-4--Adobe Codes of Condu">Responsible Business Alliance’s Code of Conduct</a>, that details our expectations for all business partners. Topics covered in the Business Partner Code of Conduct include integrity and compliance with laws; labor standards; environmental standards; and procedures. We administer risk assessments and leverage the Responsible Business Alliance assessment program to identify potential corporate responsibility risks within our supply chain.</p>
              <h3 id="anti-corruption-compliance-and-training" class="heading-s">Anti-corruption Compliance and Training</h3>
              <p class="body-m">Adobe is committed to conducting business free from extortion, bribery and all unlawful, unethical or fraudulent activity. Adobe partners must not offer, give, promise or authorize any bribe, gift, loan, fee, reward or other advantage to any government official or employee, any customer, any Adobe employee or any other person to obtain any business advantage or improperly influence any action or decision. Adobe business partners, suppliers and vendors must comply with all applicable international anti-corruption laws and regulations, including the <a href="http://www.justice.gov/criminal/fraud/fcpa/" data-http-link="true" daa-ll="U S Foreign Corrupt -5--Anti corruption Comp">U.S. Foreign Corrupt Practices Act</a> and the <a href="http://www.legislation.gov.uk/ukpga/2010/23/contents" data-http-link="true" daa-ll="U K Bribery Act-6--Anti corruption Comp">U.K. Bribery Act</a>.</p>
